        <h3>Pelawaththa Sugar Project</h3>
        <p>
            Currently, mature Sugar-Canes are harvested using a combination of manual and mechanical methods. In Pelwaththa Sugar 
            Company, maturity is determined through eye estimation and random samples. The harvesting process involves the use of large 
            machines on the entire tract, guided by these samples and observations. The operation of starting and stopping these machines 
            incurs significant costs, leading to continuous harvesting across the entire tract, even including un-matured sugar-canes. 
            Consequently, this process reduces sugar production and incurs profit loss. Thus, it becomes crucial for Pelwaththa Sugar Company 
            to pinpoint the most mature areas and identify the optimal time for harvesting. To tackle this issue, Geo Tech-Hub is devising a 
            solution that involves investigating the most suitable harvesting period and identifying the best areas for harvest. This is 
            achieved by leveraging multispectral drone imagery and ground samples to enhance decision-making.
        </p>

       <h3>Low-cost GNNS Receiver Project</h3>
       <p>
        Currently, GNSS service is used in different applications and with different types of GNSS receivers on the market. 
        In survey-grade GNSS receivers, the price of the receivers is one of the main drawbacks. To find a solution, we are currently 
        developing a low-cost GNSS receiver that can archive centimeter-level position accuracy for all kinds of survey applications. 
        It may be a good solution for survey applications.
       </p>

       <h3>Land and Property Information System â€“ A web app for UDA </h3>
       <p>
        The Land and Property Information System (LPIS) is a forward-looking initiative proposed by Sri Lanka's Urban 
        Development Authority (UDA) to enhance urban land development and management practices. By transitioning from traditional methods to 
        a digitally integrated approach, LPIS aims to streamline and monitor changes in land-related activities. LPIS serves as a unified 
        platform to provide a comprehensive overview of UDA lands and properties. Its primary objectives encompass recording and managing 
        critical data associated with acquired properties, land allocations, pending allocations, compensation records, outstanding dues, 
        revenue recovery, property dispositions, relocation specifics, and current land availability. Shifting from manual to digital document 
        management, LPIS mitigates the risk of data loss, elevates data accessibility, and simplifies data retrieval processes. This leap from 
        conventional practices addresses the limitations of manual systems, where accessing vital information, potential data loss, and 
        cumbersome coordination among departments pose challenges. Through LPIS, not only does UDA modernize its land management practices, 
        but it also fosters seamless collaboration across internal divisions such as finance, Information Technology, Human Resource Management,
         Internal Audit, Legal, and Engineering. This collaborative synergy accelerates decision-making processes and enhances overall 
         operational efficiency. In essence, the Land and Property Information System (LPIS) marks a significant stride toward effective 
         urban land development and management in Sri Lanka. By digitally centralizing crucial information, LPIS empowers UDA to make informed 
         decisions, optimize operations, and ensure the sustainable and efficient use of urban lands and properties.
       </p>

       <h3>City Data collector App</h3>
       <p>
        The City Data Collector, an Android app which is under development by Geotech Hub, bridges InfoBhoomi Web-GIS and mobile platforms. 
        It offers direct database access, aiding real-time editing of attributes for login authorities. Upgraded with survey-grade positioning 
        data from low-cost GNSS receivers, it enhances accuracy. Geotech Hub strives to integrate GNSS for survey-grade accuracy. Designed for 
        field officers and InfoBhoomi users, it empowers informed decisions on-the-go. This innovation is a step toward revolutionizing spatial 
        data collection and management. It encapsulates Geotech Hub's commitment to InfoBhoomi's advancement, promising efficient field 
        performance.
       </p>

       <h3>City 3D web platform </h3>
       <p>
        The City 3D Web-GIS interface is an ongoing research endeavor that seeks to seamlessly integrate building and apartment information 
        into the InfoBhoomi System database. While InfoBhoomi currently manages this data, the City 3D project introduces a novel dimension 
        by visualizing it within the InfoBhoomi web platform as a 3D model. Through a dynamic linkage, users can access apartment details 
        within the InfoBhoomi workspace, which then connects to 3D building models created from lidar point clouds and building footprints. 
        Geo Tech-Hub is committed to evolving this innovative project, aiming to establish a realistic 3D visual web system that transforms 
        building and apartment information management within InfoBhoomi.
       </p>
